    private void btnsalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsalvarActionPerformed
        // botao salvar
        try {
            Tarefa obj = new Tarefa();
        
        obj.setTitulo(txt_titulo.getText());
        obj.setDescricao(txt_descricao.getText());
        obj.setDatas(txt_data.getText());
        obj.setEstatus(cbstatus.getSelectedItem().toString());
        
        TarefaDAO dao = new TarefaDAO();
        
        dao.cadastrarTarefa(obj);
        
        new Helpers().LimparTela(painel_dados);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Preencha todos os dados!!!");
        }
        
        
    }//GEN-LAST:event_btnsalvarActionPerformed

