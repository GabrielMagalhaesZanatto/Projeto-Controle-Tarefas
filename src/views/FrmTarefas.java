    private void tabelaTarefasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaTarefasMouseClicked
        jTabbedPane2.setSelectedIndex(0);
        
        txt_codigo.setText(tabelaTarefas.getValueAt(tabelaTarefas.getSelectedRow(),0).toString());
        txt_titulo.setText(tabelaTarefas.getValueAt(tabelaTarefas.getSelectedRow(),1).toString());
        txt_descricao.setText(tabelaTarefas.getValueAt(tabelaTarefas.getSelectedRow(),2).toString());
        txt_data.setText(tabelaTarefas.getValueAt(tabelaTarefas.getSelectedRow(),3).toString());
        cbstatus.setSelectedItem(tabelaTarefas.getValueAt(tabelaTarefas.getSelectedRow(),4).toString());
    }//GEN-LAST:event_tabelaTarefasMouseClicked

    private void btnalterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnalterarActionPerformed
        //botao alterar
        try {
            Tarefa obj = new Tarefa();
            
            obj.setTitulo(txt_titulo.getText());
            obj.setDescricao(txt_descricao.getText());
            obj.setDatas(txt_data.getText());
            obj.setEstatus(cbstatus.getSelectedItem().toString());
            
            obj.setId(Integer.parseInt(txt_codigo.getText()));
            
            TarefaDAO dao = new TarefaDAO();
            
            dao.alterarTarefa(obj);
            
            new Helpers().LimparTela(painel_dados);
            
            CarregarTabela();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Selecione a Tarefa que deseja alterar!!!");
        }
    }//GEN-LAST:event_btnalterarActionPerformed

    private void btnexcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnexcluirActionPerformed
        //botao excluir
        try {
            int id = Integer.parseInt(txt_codigo.getText());
            TarefaDAO dao = new TarefaDAO();
            dao.excluirTarefa(id);
            
            new Helpers().LimparTela(painel_dados);
            
            CarregarTabela();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Selecione a Tarefa que deseja excluir!!!");
        }
    }//GEN-LAST:event_btnexcluirActionPerformed

    private void btnpesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnpesquisarActionPerformed
        //botao pesquisar
        String titulo = "%" + txtpesquisar.getText() + "%";
        
        TarefaDAO dao = new TarefaDAO();
        List<Tarefa> lista = dao.buscarTarefaPeloTitulo(titulo);
        
        DefaultTableModel conteudo = (DefaultTableModel) tabelaTarefas.getModel();
        conteudo.setNumRows(0);
        
        for(Tarefa tarefa :  lista){
                conteudo.addRow(new Object[]{
                 tarefa.getId(),
                 tarefa.getTitulo(),
                 tarefa.getDescricao(),
                 tarefa.getDatas(),
                 tarefa.getEstatus()
                });
            }
    }//GEN-LAST:event_btnpesquisarActionPerformed

